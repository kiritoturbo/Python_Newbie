''' nhập vào 2 số thực và in ra : 
tổng , hiệu , tích của 2 số đó '''
bienA = float(input('nhập số thực thứ nhất: '))
bienB = float(input('nhập số thực thứ hai: '))
print('Tổng của 2 số trên là', bienA + bienB)
print('Hiệu của 2 số trên là', bienA -bienB)
print('Tích của 2 số trên là', int(bienA * bienB))
print(f'thương của 2 số trên là {bienA/bienB}')